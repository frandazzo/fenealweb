public class UiAppDelega {

    private long id;
    private String provincia;
    private String causaleIscrizione;
    private String settore;
    private String ente;
    private String azienda;
    private String collaboratore;
    private String idLavoratore;
    private String note;

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getProvincia() {
        return provincia;
    }

    public void setProvincia(String provincia) {
        this.provincia = provincia;
    }

    public String getCausaleIscrizione() {
        return causaleIscrizione;
    }

    public void setCausaleIscrizione(String causaleIscrizione) {
        this.causaleIscrizione = causaleIscrizione;
    }

    public String getSettore() {
        return settore;
    }

    public void setSettore(String settore) {
        this.settore = settore;
    }

    public String getEnte() {
        return ente;
    }

    public void setEnte(String ente) {
        this.ente = ente;
    }

    public String getAzienda() {
        return azienda;
    }

    public void setAzienda(String azienda) {
        this.azienda = azienda;
    }

    public String getCollaboratore() {
        return collaboratore;
    }

    public void setCollaboratore(String collaboratore) {
        this.collaboratore = collaboratore;
    }

    public String getIdLavoratore() {
        return idLavoratore;
    }

    public void setIdLavoratore(String idLavoratore) {
        this.idLavoratore = idLavoratore;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }
}
