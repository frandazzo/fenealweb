package applica.feneal.admin.viewmodel.dbnazionale;

public class UiInvioDbNazionale {

    private String regione = "";
    private String provincia= "";
    private String anno= "";
    private String primoSemestre= "";
    private String secondoSemestre= "";
    private String edile= "";
    private String impiantiFissi= "";
    private String inps= "";

    public String getRegione() {
        return regione;
    }

    public void setRegione(String regione) {
        this.regione = regione;
    }

    public String getProvincia() {
        return provincia;
    }

    public void setProvincia(String provincia) {
        this.provincia = provincia;
    }

    public String getAnno() {
        return anno;
    }

    public void setAnno(String anno) {
        this.anno = anno;
    }

    public String getPrimoSemestre() {
        return primoSemestre;
    }

    public void setPrimoSemestre(String primoSemestre) {
        this.primoSemestre = primoSemestre;
    }

    public String getSecondoSemestre() {
        return secondoSemestre;
    }

    public void setSecondoSemestre(String secondoSemestre) {
        this.secondoSemestre = secondoSemestre;
    }

    public String getEdile() {
        return edile;
    }

    public void setEdile(String edile) {
        this.edile = edile;
    }

    public String getImpiantiFissi() {
        return impiantiFissi;
    }

    public void setImpiantiFissi(String impiantiFissi) {
        this.impiantiFissi = impiantiFissi;
    }

    public String getInps() {
        return inps;
    }

    public void setInps(String inps) {
        this.inps = inps;
    }
}
