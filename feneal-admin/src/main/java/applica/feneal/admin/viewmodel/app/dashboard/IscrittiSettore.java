public class IscrittiSettore {
    private int anno;
    private int edile;
    private int impiantiFissi;
    private int inps;


    public int getAnno() {
        return anno;
    }

    public void setAnno(int anno) {
        this.anno = anno;
    }

    public int getEdile() {
        return edile;
    }

    public void setEdile(int edile) {
        this.edile = edile;
    }

    public int getImpiantiFissi() {
        return impiantiFissi;
    }

    public void setImpiantiFissi(int impiantiFissi) {
        this.impiantiFissi = impiantiFissi;
    }

    public int getInps() {
        return inps;
    }

    public void setInps(int inps) {
        this.inps = inps;
    }
}
